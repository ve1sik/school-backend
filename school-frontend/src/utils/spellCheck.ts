const PRE_PRI_ACTUAL_ERRORS = PRE_PRI_ERRORS.filter(([wrong, correct]) => wrong !== correct && correct !== 'прибывать/пребывать');

function tokenize(text: string): string[] {
  return text.toLowerCase().match(/[а-яёa-z]+/gi) || [];
}

export function checkSpelling(text: string): SpellError[] {
  if (!text || text.trim().length < 3) return [];

  const words = tokenize(text);
  const errors: SpellError[] = [];
  const seen = new Set<string>();

  for (const word of words) {
    if (seen.has(word)) continue;
    seen.add(word);

    // Проверка словарных слов
    const vocabMatch = VOCAB_ERRORS.find(([wrong]) => wrong === word);
    if (vocabMatch) {
      errors.push({ word, suggestion: vocabMatch[1], rule: vocabMatch[2] });
      continue;
    }

    // Проверка при-/пре-
    const ppMatch = PRE_PRI_ACTUAL_ERRORS.find(([wrong]) => wrong === word);
    if (ppMatch) {
      errors.push({ word, suggestion: ppMatch[1], rule: ppMatch[2] });
    }
  }

  return errors;
}

export function highlightErrors(text: string, errors: SpellError[]): string {
  if (!errors.length) return text;
  let result = text;
  for (const err of errors) {
    const regex = new RegExp(`\\b${err.word}\\b`, 'gi');
    result = result.replace(regex, `<mark class="bg-rose-100 text-rose-700 rounded px-0.5">${err.word}</mark>`);
  }
  return result;
}
